class User {
  /**
   * Initialize the user
   * @param {string} name name of the user
   * @param {string} surname surname of the user
   * @param {string} email email of the user
   */
  constructor(name, surname, email) {
    this.name = name;
    this.surname = surname;
    this.email = email;
  }
}

export default User;
